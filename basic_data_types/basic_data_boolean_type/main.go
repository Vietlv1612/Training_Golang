package main

func main() {
	// Define boolean variable
	var isTrue bool = true
	// Print boolean variable
	println(isTrue)
	// Define boolean variable
	isFalse := false
	// Print boolean variable
	println(isFalse)

	// Expression with boolean
	println(isTrue && isFalse)
	println(isTrue || isFalse)
	println(!isTrue)
}
